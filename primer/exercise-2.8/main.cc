#include <iostream>

int main()
{
    std::cout << "First let's print 2M followed by a newline:" << std::endl << std::endl;

    std::cout << "2M\n";

    std::cout << std::endl;

    std::cout << "Now let's print 2, then a tab, then an M, followed by a newline:" << std::endl << std::endl;

    std::cout << "2\tM\n";

    std::cout << std::endl;
    
    return 0;
}

